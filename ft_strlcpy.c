#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t len)
{
	unsigned int	i;
	int				ptr;

	ptr = ft_strlen(src);
	i = 0;
	if (len == 0)
		return (ptr);
	while (src[i] && i < len - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ptr);
}
